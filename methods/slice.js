const arr = [1, 2, 3, 4, 5];
const sliced = arr.slice(1, 4); // Copies elements from index 1 to index 3
console.log(sliced); // Output: [2, 3, 4]
console.log(arr);    // Output: [1, 2, 3, 4, 5]
//Returns a shallow copy of a portion of the array. The original array is not modified. end is exclusive.

